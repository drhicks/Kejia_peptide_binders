alphabet = 'ARNDCQEGHILKMFPSTWYVBZX-'

blosum62 = np.loadtxt(StringIO(blosum62_txt))

#print(blosum62.shape)

def compute_blosum62_score(query, target, pos=[]):
    assert(len(query)==len(target))
    if len(pos) == 0:
        pos = range(len(query))
    score = 0
    for i in pos:
        score += blosum62[alphabet.index(query[i]), alphabet.index(target[i])]
    return score*1.0/len(query)


def main():
    seq1 = sys.argv[1]
    seq2 = sys.argv[2]
    print( compute_blosum62_score(seq1, seq2) )


if __name__ == '__main__':
    main()
